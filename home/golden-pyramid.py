def count_gold(pyramid):
    l = [pyramid[0][0]]
    for i in range(1, len(pyramid)):
        l.append(l[-1] + pyramid[i][-1])
        for j in range(len(pyramid[i])-2, 0, -1):
            l[j] = max(l[j], l[j-1])+pyramid[i][j]
        l[0] += pyramid[i][0]
    return max(l)


